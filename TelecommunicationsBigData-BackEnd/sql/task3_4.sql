SELECT COUNT(*) AS TCOUNT, NETWORK_NAME, NETWORK_TYPE 
FROM DATACONNECTION 
WHERE PING_VALUE = '11111'
GROUP BY NETWORK_NAME, NETWORK_TYPE 
LIMIT 10;

-- 运营商可选
-- 日期（范围）
SELECT COUNT(*) AS TCOUNT, NETWORK_NAME, NETWORK_TYPE 
FROM DATACONNECTION 
WHERE PING_VALUE = '11111'
AND NETWORK_NAME = 'CMCC'
AND TIME_INDEX_CLIENT BETWEEN 20170101 AND 20180202
GROUP BY NETWORK_NAME, NETWORK_TYPE 
LIMIT 10;
