SELECT USER_LAT, USER_LON, NETWORK_NAME, MOBILE_NETWORK_TYPE
FROM DATACONNECTION 
WHERE NETWORK_TYPE = 'Wi-Fi' AND NETWORK_NAME = 'CMCC' AND TIME_INDEX_CLIENT BETWEEN 20170101 AND 20180202
AND PING_VALUE = '00000'
LIMIT 10;

-- 网络格式式可选  NETWORK_TYPE
-- 运营商可选    NETWORK_NAME 
-- 日期（范围）  TIME_INDEX_CLIENT
SELECT USER_LAT, USER_LON, NETWORK_NAME, MOBILE_NETWORK_TYPE
FROM DATACONNECTION 
WHERE NETWORK_TYPE = 'Wi-Fi' 
AND NETWORK_NAME = 'CMCC' 
AND TIME_INDEX_CLIENT BETWEEN 20170101 AND 20180202
AND PING_VALUE = '11111'
LIMIT 10;


