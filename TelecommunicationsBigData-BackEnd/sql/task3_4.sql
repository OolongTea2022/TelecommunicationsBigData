SELECT COUNT(*) AS COUNT, PROVINCE
FROM NWQUALITY 
GROUP BY PROVINCE
ORDER BY COUNT DESC
LIMIT 10;

-- 运营商可选
-- 日期（范围）
SELECT COUNT(*) AS COUNT, PROVINCE
FROM NWQUALITY
WHERE NWOPERATOR = 'CMCC'
AND DAYTIME BETWEEN 2017010100 AND 2018020200
GROUP BY PROVINCE
ORDER BY COUNT DESC
LIMIT 10;


